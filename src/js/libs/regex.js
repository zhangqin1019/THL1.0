var NAME=/^([a-zA-Z0-9]|[\u4e00-\u9fa5]){3,50}$/g;  //包含字母、数字或中文，长度为3-50
var DATE=/^[1-9]\d{3}-(0?[1-9]|1[0-2])-((0?[1-9])|((1|2)[0-9])|30|31)$/g;  //日期格式